void swap(int *arr, int i, int j)
{
    int temp;
    temp = arr[i];
    arr[i] = arr[j];
    arr[j] = temp;
}

void bubbleSort(int *arr, int size)
{
    int n, i, j;
    n = size;
    for (int i = 0; i <= n - 1; i++)
    {
        for (int j = 0; j <= n - i - 1; j++)
        {
            if (arr[j] > arr[j + 1])
            {
                swap(arr, j, j + 1);
            }
        }
    }
}

void merge(int *arr, int l, int m, int r)
{
    int n1, n2, i, j, k;
    n1 = m - l + 1;
    n2 = r - m;
    int L[n1];
    int R[n2];
    for (int i = 0; i <= n1 - 1; i++)
    {
        L[i] = arr[l + i];
    }
    for (int j = 0; j <= n2 - 1; j++)
    {
        R[j] = arr[m + 1 + j];
    }
    i = 0;
    j = 0;
    k = l;
    while (i < n1 && j < n2)
    {
        if (L[i] <= R[j])
        {
            arr[k] = L[i];
            i = i + 1;
        }
        else
        {
            arr[k] = R[j];
            j = j + 1;
        }

        k = k + 1;
    }

    while (i < n1)
    {
        arr[k] = L[i];
        i = i + 1;
        k = k + 1;
    }

    while (j < n2)
    {
        arr[k] = R[j];
        j = j + 1;
        k = k + 1;
    }
}

void mergeSort(int *arr, int l, int r)
{
    int m;
    if (l < r)
    {
        m = l + (r - l) / 2;
        mergeSort(arr, l, m);
        mergeSort(arr, m + 1, r);
        merge(arr, l, m, r);
    }
}

int binarySearch(int *arr, int size, int x)
{
    int l, r, mid;
    l = 0;
    r = size - 1;
    while (l <= r)
    {
        mid = l + (r - l) / 2;
        if (arr[mid] == x)
        {
            return mid;
        }

        if (arr[mid] < x)
        {
            l = mid + 1;
        }
        else
        {
            r = mid - 1;
        }
    }

    return -1;
}

